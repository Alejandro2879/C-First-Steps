#include "3-calc.h"

/**
 * op_add - Add 2 numbers.
 * @a: First number.
 * @b: Second number.
 * Return: a + b.
 */

int op_add(int a, int b) { return(a + b); }

/**
 * op_sub - substrac b from a.
 * @a: First number.
 * @b: Second number.
 * Return: a - b.
 */

int op_sub(int a, int b) { return(a - b); }

/**
 * op_mul - Multyply 2 numbers.
 * @a: First number.
 * @b: Second number.
 * Return: a * b.
 */

int op_mul(int a, int b) { return(a * b); }

/**
 * op_div - divide a by b.
 * @a: First number.
 * @b: Second number.
 * Return: a / b.
 */

int op_div(int a, int b) { return(a / b); }

/**
 * op_mod - Computes a % b.
 * @a: First number.
 * @b: Second number.
 * Return: a % b.
 */

int op_mod(int a, int b) { return(a % b); }
